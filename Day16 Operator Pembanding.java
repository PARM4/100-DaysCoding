public class OperatorPembanding {

    public static void main(String[] args) {
 
 /*
     Operator Pembanding
      > lebih besar
      < lebih kecil
      >= lebih besar sama dengan
      <= lebih kecil sama dengan
      == sama dengan
      != tidak sama dengan 
 */
 
        int nilaiA = 7;
        int nilaiB = 5;
        boolean hasil;

        hasil = nilaiA > nilaiB;
        System.out.println(hasil);

        hasil = nilaiA < nilaiB;
        System.out.println(hasil);

        hasil = nilaiA >= nilaiB;
        System.out.println(hasil);

        hasil = nilaiA <= nilaiB;
        System.out.println(hasil);

        hasil = nilaiA == nilaiB;
        System.out.println(hasil);

        hasil = nilaiA != nilaiB;
        System.out.println(hasil);

    }

}
