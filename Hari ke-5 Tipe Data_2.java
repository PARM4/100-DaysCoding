package tipe.data2;

public class TipeData2 {
    public static void main(String[] args) {
    /*
      Tipe Data Angka Bilangan Berkoma
       Floating Point           -> float    -> 32 bit
       Double Floating Point    -> double   -> 54 bit
    */

    float berat = 12.5f;
    double tinggi = 167.67;
    
    System.out.println("Berat = " + berat + " kg");
    System.out.println("Tinggi = " + tinggi + " cm");

    }   
}
