package day44;

public class Main {
    
    public static void main(String[] args) {
        //Penggunaan Looping pada Array
        
        int[] a ={2,5,6,5,1,4,7,8};
        int jml = a.length;
        
        for(int i = 0; i < jml; i++){
            System.out.println(a[i]);
        }
    }
    
}
